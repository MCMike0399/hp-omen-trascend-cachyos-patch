else
    echo -e "  ${YELLOW}Skipped (acpi_call not available)${NC}"
fi
echo ""

###############################################################################
# Step 4: Restart nvidia-powerd and measure
###############################################################################
echo -e "${BOLD}[4/4] Restarting nvidia-powerd + measuring power${NC}"
systemctl restart nvidia-powerd
sleep 2

echo -e "  Sampling GPU power (5 readings, 1s apart):"
echo -e "  ${CYAN}power.draw, enforced.limit, max.limit, clocks, util%, temp${NC}"
for i in 1 2 3 4 5; do
    nvidia-smi --query-gpu=power.draw,enforced.power.limit,power.max_limit,clocks.gr,utilization.gpu,temperature.gpu --format=csv,noheader 2>/dev/null
    sleep 1
done

echo ""
echo -e "${BOLD}=== Final State ===${NC}"
nvidia-smi -q -d POWER,PERFORMANCE 2>/dev/null | grep -E "Performance State|Power|SW Power Cap" | head -10
echo ""

if [[ -f /sys/kernel/debug/ec/ec0/io ]]; then
    EC_FINAL=$(dd if=/sys/kernel/debug/ec/ec0/io bs=1 count=1 skip=$((0x95)) 2>/dev/null | xxd -p)
    echo -e "EC 0x95 final: 0x${EC_FINAL}"
fi
