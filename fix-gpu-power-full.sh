        RESULT=$(cat /proc/acpi/call 2>/dev/null | tr -d '\0')
        if [[ "$RESULT" != *"Error"* && "$RESULT" != *"not found"* && -n "$RESULT" ]]; then
            echo -e "  ${GREEN}Found WMI method: ${METHOD}${NC}"
            echo -e "  GET GPU modes result: $RESULT"
            WMI_METHOD="$METHOD"
            break
        fi
    done

    if [[ -z "${WMI_METHOD:-}" ]]; then
        echo -e "  ${YELLOW}Could not find WMI method via acpi_call${NC}"
        echo -e "  ${YELLOW}Falling back to kernel module approach${NC}"
    else
        # Now SET GPU modes: ctgp=1, ppab=1, dstate=1, gpu_slowdown_temp=96
        echo -e "  Setting ctgp=1, ppab=1, dstate=1..."
        echo "${WMI_METHOD} 0 1 {0x53,0x45,0x43,0x55,0x0D,0x00,0x00,0x00,0x22,0x00,0x00,0x00,0x04,0x00,0x00,0x00,0x01,0x01,0x01,0x60,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00}" > /proc/acpi/call 2>/dev/null
        SET_RESULT=$(cat /proc/acpi/call 2>/dev/null | tr -d '\0')
        echo -e "  SET result: $SET_RESULT"

        # Verify
        echo "${WMI_METHOD} 0 2 {0x53,0x45,0x43,0x55,0x0D,0x00,0x00,0x00,0x21,0x00,0x00,0x00,0x04,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00}" > /proc/acpi/call 2>/dev/null
        VERIFY_RESULT=$(cat /proc/acpi/call 2>/dev/null | tr -d '\0')
        echo -e "  Verify GET result: $VERIFY_RESULT"
    fi
else
    echo -e "  ${RED}acpi_call module not available${NC}"
    echo -e "  Install it: ${CYAN}sudo pacman -S acpi_call${NC}"
    echo ""
    echo -e "  ${YELLOW}Alternative: Patching hp-wmi to add ctgp support for Omen boards${NC}"
fi
echo ""

###############################################################################
# Step 3: Also write performance mode via WMI (HPWMI_SET_PERFORMANCE_MODE=0x1A)
###############################################################################
echo -e "${BOLD}[3/4] Setting thermal profile via WMI (0x1A)${NC}"
if [[ -f /proc/acpi/call ]] && [[ -n "${WMI_METHOD:-}" ]]; then
    # bios_args: sig=SECU, cmd=GM(0x0D), cmdtype=0x1A, datasize=2, data={0xFF, 0x31}
    # 0xFF = first byte (Omen Control Center sets this), 0x31 = V1 performance
    echo "${WMI_METHOD} 0 1 {0x53,0x45,0x43,0x55,0x0D,0x00,0x00,0x00,0x1A,0x00,0x00,0x00,0x02,0x00,0x00,0x00,0xFF,0x31,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00}" > /proc/acpi/call 2>/dev/null
