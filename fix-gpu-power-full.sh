    # The WMI method ID for GM commands uses mid based on output size
    # For SET commands (outsize=0), mid=1
    #
    # The ACPI method path for HP WMI BIOS is typically:
    # \_SB.WMID.WMAA or \_SB.WMI.WMBA (the WMI method)
    #
    # The bios_args struct is:
    #   u32 signature = 0x55434553 ("SECU")
    #   u32 command = 0x0D (HPWMI_GM = 0x0D from hp_wmi_command enum)
    #   u32 commandtype = 0x22 (SET_GPU_THERMAL_MODES)
    #   u32 datasize = 4 (sizeof victus_gpu_power_modes)
    #   u8  data[128]: [0x01, 0x01, 0x01, 0x60, 0x00, ...]
    #     ctgp=1, ppab=1, dstate=1, gpu_slowdown_temp=96°C (0x60)

    # First, read current GPU modes via GET (query 0x21)
    # bios_args for GET: sig=SECU, cmd=GM(0x0D), cmdtype=0x21, datasize=4, data={0...}
    # For outsize=4, mid=2

    # Build the ACPI call for WMI evaluate
    # The WMI method path depends on the BIOS implementation
    # Try common HP paths
    echo -e "  Trying to read current GPU thermal modes..."

    # Try to find the correct WMI method path
    for METHOD in "\\_SB.WMID.WMAA" "\\_SB.WMI2.WMBA" "\\_SB.WMI.WMBA" "\\_SB.WMID.WMBA"; do
