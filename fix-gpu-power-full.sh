#!/bin/bash
# fix-gpu-power-full.sh - Full GPU power unlock for HP Omen Transcend 14
#
# ROOT CAUSE: The Omen code path in hp-wmi sets the thermal profile via WMI
# but does NOT enable ctgp (Configurable TGP) or ppab (Power Performance
# Adjustment Boost). The Victus S path DOES enable these, getting full 65W.
#
# Without ctgp enabled, nvidia-powerd Dynamic Boost only gets ~10W above
# the 35W base (=~45W), instead of the full 30W headroom (=65W).
#
# This script:
# 1. Writes EC 0x95 = 0x31 (performance mode) directly
# 2. Enables ctgp + ppab via WMI query 0x22 (SET_GPU_THERMAL_MODES)
# 3. Restarts nvidia-powerd to renegotiate power
set -euo pipefail

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
BOLD='\033[1m'
NC='\033[0m'

if [[ $EUID -ne 0 ]]; then
    echo -e "${RED}Must run as root: sudo $0${NC}"
    exit 1
fi

echo -e "${BOLD}=== Full GPU Power Unlock (HP Omen Transcend 14 - 8C58) ===${NC}"
echo ""

###############################################################################
# Step 1: Direct EC write - set performance mode
###############################################################################
echo -e "${BOLD}[1/4] Setting EC thermal profile to Performance${NC}"
modprobe ec_sys write_support=1 2>/dev/null || true

if [[ -f /sys/kernel/debug/ec/ec0/io ]]; then
    EC_BEFORE=$(dd if=/sys/kernel/debug/ec/ec0/io bs=1 count=1 skip=$((0x95)) 2>/dev/null | xxd -p)
    echo -e "  EC 0x95 before: 0x${EC_BEFORE}"

    printf '\x31' | dd of=/sys/kernel/debug/ec/ec0/io bs=1 count=1 seek=$((0x95)) 2>/dev/null
    sleep 0.3

    EC_AFTER=$(dd if=/sys/kernel/debug/ec/ec0/io bs=1 count=1 skip=$((0x95)) 2>/dev/null | xxd -p)
    echo -e "  EC 0x95 after:  0x${EC_AFTER}"

    if [[ "$EC_AFTER" == "31" ]]; then
        echo -e "  ${GREEN}EC performance mode set${NC}"
    else
        echo -e "  ${RED}EC write reverted to 0x${EC_AFTER}!${NC}"
    fi
else
    echo -e "  ${RED}Cannot access EC debugfs${NC}"
fi
echo ""

###############################################################################
# Step 2: Enable ctgp + ppab via WMI (the missing piece!)
###############################################################################
echo -e "${BOLD}[2/4] Enabling GPU ctgp + ppab via WMI${NC}"

# We need to call hp_wmi_perform_query(0x22, HPWMI_GM, {ctgp=1, ppab=1, dstate=1, temp=keep})
# This requires either:
# a) acpi_call module to call the WMI method directly
# b) A kernel module patch to add ctgp support for Omen boards
# c) A small helper program that calls WMI via ioctl

# Method A: acpi_call
if ! lsmod | grep -q acpi_call; then
    modprobe acpi_call 2>/dev/null || true
fi

if [[ -f /proc/acpi/call ]]; then
    echo -e "  ${GREEN}acpi_call module available${NC}"

    # The WMI BIOS method is at \_SB.WMID._WED or through wmi_evaluate_method
